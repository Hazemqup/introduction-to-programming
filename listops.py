the_num_list=[10,20,32,50,70,100,200,400,550,1000]

def number_list(num):
    print("The list is:", list(num))

def sum_of_numbers(num):
    print("The total of all the numbers:", sum(num))

def largest_number(num):
    print("The most largest number is:", max(num))

def reverse_the_list(num):
    print("The list in reverse:", list(reversed(num)))

def smallest_number(num):
    print("The smallest number is:", min(num))




number_list(the_num_list)
sum_of_numbers(the_num_list)
largest_number(the_num_list)
reverse_the_list(the_num_list)
smallest_number(the_num_list)
